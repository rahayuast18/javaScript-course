// For loop
for(let k = 0; k < 5; k++) {
    console.log(k);
}

// For of loop
let myArray = ["Luke", "Han", "Chewbacca", "Leia"];

for(const arrayItem of myArray) {
    console.log(arrayItem)
}

// While and do-while
let i = 1;

while (i <= 100) {
    console.log(i);
    i++;
}

let j = 1;

do {
    console.log(j);
    j++;
} while (j <= 100);

